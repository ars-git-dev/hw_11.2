def capital_string(input_string):
    """Эта функция принимает на вход строку и возвращает
    заглавными все буквы в строке"""
    return input_string.upper()

def capital_words(input_string):
    """Эта функция принимает на вход строку и возвращает
    заглавными первые буквы каждого слова в строке"""
    return input_string.title()
